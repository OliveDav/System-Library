    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int eliminar = jTable1.getSelectedRow();
        
        if(eliminar > -1){
            
            if(JOptionPane.showConfirmDialog(this, "Estas seguro de eliminar")==0){
            ProyectProg1.cupones.remove(eliminar);
            pintarTabla();
            }
        }else{
            JOptionPane.showMessageDialog(this, "Selecciones un usuario valido para eliminar");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try{
        if (cupones != null) {
         cupones.codigo = jTextField1.getText();
         cupones.descuento = Double.parseDouble(jTextField2.getText());
            if (choice1.getSelectedItem().equals("Monto")) {
                cupones.tipoDesc = "Monto";
            } else {
                cupones.tipoDesc = "Porcentaje";
            }
            cupones.fechVen = jTextField3.getText();
            pintarTabla();
            JOptionPane.showMessageDialog(this, "Se actualizo el cupon correctamente");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            choice1.select(0);
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Ingrese los valores correctamente");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (jTable1.getSelectedRow()>=0) {
            cupones = ProyectProg1.cupones.get(jTable1.getSelectedRow());
            jTextField1.setText(cupones.codigo);
            jTextField2.setText(String.valueOf(cupones.descuento));
            if(cupones.tipoDesc.equals("Monto")){
                choice1.select(0);
            }else{
                choice1.select(1);
            }
            jTextField3.setText(cupones.fechVen);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

