    private void pintarTabla(){
        String encabezado[] = {"Codigo", "Descuento", "Tipo", "Fecha de vencimiento"};
        DefaultTableModel t = new DefaultTableModel(encabezado, ProyectProg1.cupones.size());
        jTable1.setModel(t);
        TableModel tabla= jTable1.getModel();
        
        for(int i = 0; i < ProyectProg1.cupones.size();i++){
            Cupones u = ProyectProg1.cupones.get(i);
            tabla.setValueAt(u.codigo, i, 0);
            tabla.setValueAt(u.descuento, i, 1);
            tabla.setValueAt(u.tipoDesc, i, 2);
            tabla.setValueAt(u.fechVen, i, 3);
        }
    }
    
    public static void leer(String ruta){
        try {
            File archivo = new File(ruta);
            FileReader leer = new FileReader(archivo);
            BufferedReader bloque = new BufferedReader(leer);
            String linea = bloque.readLine();
            while(linea != null){
                String[] cup = linea.split("\\|");
                Cupones c = new Cupones();
                c.codigo = cup[0].trim();
                c.descuento = Double.parseDouble(cup[1].trim());
                c.tipoDesc = cup[2].trim();
                c.fechVen = cup[3].trim();
                ProyectProg1.cupones.add(c);
                linea = bloque.readLine();
            }
            leer.close(); 
        } catch (IOException | NumberFormatException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
    }
