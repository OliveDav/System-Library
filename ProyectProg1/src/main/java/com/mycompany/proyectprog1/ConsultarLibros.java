    private void pintarTabla(){
        String encabezado[] = {"Titulo", "Autor", "Genero", "Precio sin IVA", "Precio Venta", "Stock"};
        DefaultTableModel t = new DefaultTableModel(encabezado, ProyectProg1.libros.size());
        jTable1.setModel(t);
        TableModel tabla= jTable1.getModel();
        
        for(int i = 0; i < ProyectProg1.libros.size();i++){
            Libros u = ProyectProg1.libros.get(i);
            tabla.setValueAt(u.titulo, i, 0);
            tabla.setValueAt(u.autor, i, 1);
            tabla.setValueAt(u.genero, i, 2);
            tabla.setValueAt(u.precioSinIva, i, 3);
            tabla.setValueAt(u.precioVenta, i, 4);
            tabla.setValueAt(u.stock, i, 5);
        }
    }

