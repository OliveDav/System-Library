    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        LibrosVentas lb = new LibrosVentas();
        double total = 0;
        double subtotal;
        lb.Libro = jComboBox1.getSelectedItem().toString();
        lb.Cantidad = Integer.parseInt(jTextField4.getText());
        for(Libros l : ProyectProg1.libros){
            if(l.titulo.equals(jComboBox1.getSelectedItem())){
                double precio = l.precioVenta;
                int cant = Integer.parseInt(jTextField4.getText());
                subtotal = precio * cant;
                lb.subtotal = subtotal;
            }   
        }
        ProyectProg1.librosventas.add(lb);
        for(LibrosVentas lb1 : ProyectProg1.librosventas){
            total += lb1.subtotal;
        }
        
        jLabel6.setText("Q."+total);
        pintarTabla();
    }//GEN-LAST:event_jButton3ActionPerformed

