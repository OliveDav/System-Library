    public void pintarTabla(){
        String encabezado[] = {"Libro", "Cantidad", "Subtotal"};
        DefaultTableModel t = new DefaultTableModel(encabezado, ProyectProg1.librosventas.size());
        jTable1.setModel(t);
        TableModel tabla = jTable1.getModel();
        
        for(int i = 0; i<ProyectProg1.librosventas.size(); i++){
            LibrosVentas lib = ProyectProg1.librosventas.get(i);
            tabla.setValueAt(lib.Libro, i, 0);
            tabla.setValueAt(lib.Cantidad, i, 1);
            tabla.setValueAt(lib.subtotal, i, 2);
            
        }
    }

